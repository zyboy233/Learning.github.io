
# %s 是通用类型占位符 可以用来表示任何类型
# 凡是本职工作是用来表示字符串类型的

age = 17
# %d 数字类型的占位符
print('年龄是 %s'% age)
print('年龄是 %d' % age)
# 保留整数位 如果位数不够的话 用0补齐
print('年龄是 %.4d' % age)
pi = 3.1415926
# %f 小数的占位符
print('圆周率是%s' % pi)
# %.2f表示保留小数点后两位
print('圆周率是%.2f' % pi)

a = 10
b = 5
# % 求余数
# 任意数除以5的御书有几个? [0,1,2,3,4,]
print(a % b)
print(a / b)
a = '10'
b = '5'
print('-----------------------------------------------')
print(a + b)
